function rankTypes(text) {
  const scored = typeRules.map((rule) => {
    const evidence = rule.id === "fulltime"
      ? fulltimeEvidence(text)
      : { score: keywordHits(text, rule.keywords).length, hits: keywordHits(text, rule.keywords) };
    return {
      ...rule,
      score: evidence.score,
      hits: evidence.hits
    };
  });
  scored.sort((a, b) => b.score - a.score);
  const useful = scored.filter((item) => item.score > 0).slice(0, 2);
  return useful.length ? useful : [typeRules[0], typeRules[4]];
}

function rankCases(text, types, sprint, subjectPlans, stage) {
  const normalized = normalizeText(text);
  const primary = types[0];
  const secondary = types[1] || types[0];
  const explicitPlans = subjectPlans.filter(function (plan) { return Number.isFinite(plan.score); });
  const levelOrder = { foundation: 0, framework: 1, thinking: 2, stability: 3 };
  return Object.entries(caseCards)
    .map(function (entry, order) {
      const name = entry[0];
      const card = entry[1];
      const signals = caseSignals[name] || [];
      const hits = signals.filter(function (signal) { return normalized.includes(normalizeText(signal)); });
      let score = hits.length * 6;
      if (primary.cases.includes(name)) score += 5;
      if (secondary.cases.includes(name)) score += 2;
      const namedCase = normalized.includes(normalizeText(name));
      if (namedCase) score += 100;
      if (sprint.id === "foundation" && /低起点|基础|正反馈|文化课断层/.test(card.type + card.point)) score += 4;
      if (sprint.id === "stability" && /高分|稳定|尖子|强基|清北/.test(card.type + card.point)) score += 3;

      let incompatible = false;
      const scoreReasons = [];
      explicitPlans.forEach(function (plan) {
        const caseScore = caseSubjectScores[name] && caseSubjectScores[name][plan.subject];
        if (!Number.isFinite(caseScore)) {
          const mentionsSubject = (card.proof + card.point + signals.join(" ")).includes(plan.subject);
          if (mentionsSubject) score += 2;
          return;
        }
        const caseBand = getSubjectBand(plan.subject, caseScore, plan.levelId);
        const gap = Math.abs(levelOrder[plan.levelId] - levelOrder[caseBand.levelId]);
        const distance = Math.abs(plan.score - caseScore);
        const maxScore = subjectBandStrategies[plan.subject].maxScore;
        const sameLayerDistance = maxScore * 0.2;
        const adjacentLayerDistance = maxScore * 0.15;
        if (gap === 0 && distance <= sameLayerDistance) {
          score += 18 + Math.max(0, 8 - Math.floor(distance / 5));
          scoreReasons.push(plan.subject + "同属“" + plan.levelName + "”（当前" + plan.score + "分，案例起点" + caseScore + "分）");
        } else if (gap === 1 && distance <= adjacentLayerDistance) {
          score += 6 + Math.max(0, 4 - Math.floor(distance / 10));
          scoreReasons.push(plan.subject + "处在相邻层级且分差接近（当前" + plan.score + "分，案例起点" + caseScore + "分）");
        } else {
          incompatible = true;
          score -= 80;
        }
      });

      if (/高三|高考|一诊|二诊|三诊|全日制/.test(stage + text) && /初三|初中|中考/.test(card.type + card.proof)) {
        score -= 8;
      }

      const reasonParts = [];
      if (scoreReasons.length) reasonParts.push(scoreReasons.slice(0, 2).join("；"));
      if (hits.length) reasonParts.push("命中“" + hits.slice(0, 3).join("、") + "”");
      if (!reasonParts.length) reasonParts.push("与“" + primary.name + "”的问题结构相似");
      return { name: name, ...card, score: score, hits: hits, reason: reasonParts.join("；"), order: order, incompatible: incompatible && !namedCase };
    })
    .filter(function (item) { return !item.incompatible; })
    .sort(function (a, b) { return b.score - a.score || b.hits.length - a.hits.length || a.order - b.order; })
    .slice(0, 6);
}

