function saveHistory(data) {
  const history = readHistory();
  const item = {
    id: Date.now(),
    title: `${data.types[0].name}｜${data.stage}`,
    preview: data.sourceText.slice(0, 90),
    text: lastPlainText
  };
  localStorage.setItem("meibo-consulting-history", JSON.stringify([item, ...history].slice(0, 8)));
}

function readHistory() {
  try {
    return JSON.parse(localStorage.getItem("meibo-consulting-history") || "[]");
  } catch {
    return [];
  }
}

function renderHistory() {
  const history = readHistory();
  const list = $("#historyList");
  if (!history.length) {
    list.innerHTML = `<div class="empty-history">暂无记录</div>`;
    return;
  }
  list.innerHTML = history.map((item) => `
    <button class="history-item" type="button" data-id="${item.id}">
      <strong>${escapeHtml(item.title)}</strong>
      <span>${escapeHtml(item.preview || "已生成方案")}</span>
    </button>
  `).join("");
}

function copyText(text) {
  if (!text) {
    showToast("还没有可复制内容");
    return;
  }
  if (navigator.clipboard && window.isSecureContext) {
    navigator.clipboard.writeText(text).then(() => showToast("已复制"));
    return;
  }
  const helper = document.createElement("textarea");
  helper.value = text;
  helper.style.position = "fixed";
  helper.style.left = "-9999px";
  document.body.appendChild(helper);
  helper.focus();
  helper.select();
  document.execCommand("copy");
  helper.remove();
  showToast("已复制");
}

function downloadText(text) {
  if (!text) {
    showToast("还没有可下载内容");
    return;
  }
  const blob = new Blob([text], { type: "text/plain;charset=utf-8" });
  const link = document.createElement("a");
  link.href = URL.createObjectURL(blob);
  link.download = `美博家长沟通方案-${new Date().toISOString().slice(0, 10)}.txt`;
  document.body.appendChild(link);
  link.click();
  URL.revokeObjectURL(link.href);
  link.remove();
}

function showToast(message) {
  const toast = $("#toast");
  toast.textContent = message;
  toast.classList.add("show");
  clearTimeout(showToast.timer);
  showToast.timer = setTimeout(() => toast.classList.remove("show"), 1800);
}

function generate() {
  if (!$("#parentText").value.trim() && !selectedChips.length) {
    showToast("先输入家长描述，或选择几个关注点");
    $("#parentText").focus();
    return;
  }
  renderOutput(buildOutput());
}

function bindEvents() {
  $("#generateBtn").addEventListener("click", generate);
  $("#clearBtn").addEventListener("click", () => {
    $("#parentText").value = "";
    selectedChips = [];
    $$(".chips button").forEach((button) => button.classList.remove("active"));
    $("#result").className = "result-empty";
    $("#result").textContent = "输入家长描述后，系统会生成学生画像、沟通主线、产品动作、案例匹配和可直接转述的话术。";
    $("#resultMeta").textContent = "等待输入";
    lastPlainText = "";
  });
  $("#sampleBtn").addEventListener("click", () => {
    const next = samples[Math.floor(Math.random() * samples.length)];
    $("#parentText").value = next;
    showToast("已填入样例");
  });
  $("#copyBtn").addEventListener("click", () => copyText(lastPlainText));
  $("#downloadBtn").addEventListener("click", () => downloadText(lastPlainText));
  $("#clearHistoryBtn").addEventListener("click", () => {
    localStorage.removeItem("meibo-consulting-history");
    renderHistory();
    showToast("历史已清除");
  });
  $$(".chips button").forEach((button) => {
    button.addEventListener("click", () => {
      const value = button.dataset.chip;
      button.classList.toggle("active");
      selectedChips = $$(".chips button.active").map((item) => item.dataset.chip);
      if (!$("#parentText").value.includes(value)) {
        showToast(`已选择：${value}`);
      }
    });
  });
  $("#historyList").addEventListener("click", (event) => {
    const button = event.target.closest(".history-item");
    if (!button) return;
    const item = readHistory().find((entry) => String(entry.id) === button.dataset.id);
    if (!item) return;
    lastPlainText = item.text;
    $("#resultMeta").textContent = item.title;
    $("#result").className = "";
    $("#result").innerHTML = `<div class="result-content"><article class="result-card full"><h3>历史记录</h3><pre>${escapeHtml(item.text)}</pre></article></div>`;
  });
  $("#parentText").addEventListener("keydown", (event) => {
    if ((event.metaKey || event.ctrlKey) && event.key === "Enter") {
      generate();
    }
  });
}

bindEvents();
renderHistory();
