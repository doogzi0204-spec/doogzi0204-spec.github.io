    if (/零散|框架|体系|综合题|听懂不会做|背了不会用/.test(text)) score += level.id === "framework" ? 2 : 0;
    if (/211|985|压轴|新题|怪题|迁移|建模|重点本科/.test(text + goal)) score += level.id === "thinking" ? 2 : 0;
    if (/清北|强基|顶尖|高分|稳定|粗心|时间分配|考场/.test(text + goal)) score += level.id === "stability" ? 2 : 0;
    if (mainType.id === "top" && level.id === "thinking") score += 1;
    if (mainType.id === "mindset" && level.id === "stability" && /考试|紧张|波动|粗心|考场/.test(text)) score += 1;
    if (mainType.id === "fulltime" && level.id === "foundation") score += 1;
